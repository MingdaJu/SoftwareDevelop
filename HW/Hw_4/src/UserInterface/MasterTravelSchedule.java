    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblMaster.getSelectedRow();
        if(selectedRow>=0){
            int selectionButton = JOptionPane.YES_NO_OPTION;
            int selectionResult = JOptionPane.showConfirmDialog(null, "Are you sure to delete??","Warning",selectionButton);
            if(selectionResult == JOptionPane.YES_OPTION){
                Customer customer = (Customer)tblMaster.getValueAt(selectedRow, 0);
                customerDirectory.getCustomerList().remove(customer);
                populate();
            }
        }else{
            JOptionPane.showMessageDialog(null, "Please select a Row!!");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void populate() {
        DefaultTableModel dtm = (DefaultTableModel) tblMaster.getModel();
        dtm.setRowCount(0);
        for (Customer customer : customerDirectory.getCustomerList()) {
            Object[] row = new Object[6];
            row[0] = customer;
            row[1] = customer.getFlightNumber();
            for(int i=0; i<flightDirectory.getFlightList().size(); i++) {
                if(customer.getFlightNumber().equals(flightDirectory.getFlightList().get(i).getFlightNumber())) {
                    row[2] = flightDirectory.getFlightList().get(i).getDeparture();
                    row[3] = flightDirectory.getFlightList().get(i).getDestination();
                }
            }
            //row[2] = flight.getDeparture();
            //row[3] = flight.getDestination();
            int[] temp = new int[2];
            temp = customer.getSeat();
            if (temp[0]==0) row[4] = "Left window";
            else if (temp[0]==1) row[4] = "Left middle";   
            else if (temp[0]==2) row[4] = "Left aisle"; 
            else if (temp[0]==3) row[4] = "Right window"; 
            else if (temp[0]==4) row[4] = "Right middle"; 
            else if (temp[0]==5) row[4] = "Right window"; 
            row[5] = temp[1]+1;

            dtm.addRow(row);
        }
    }
    
