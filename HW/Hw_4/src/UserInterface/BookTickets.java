    private void btnSelectRowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelectRowActionPerformed
        // TODO add your handling code here: 
        for (int n=0; n<customerDirectory.getCustomerList().size(); n++) {
                int[] seatNum=customerDirectory.getCustomerList().get(n).getSeat();
                if(txtFlightNumber.getText().equals(customerDirectory.getCustomerList().get(n).getFlightNumber()) && seatNum[0]==jPos.getSelectedIndex()&& seatNum[1]==jRow.getSelectedIndex()) {
                    JOptionPane.showMessageDialog(null, "This seat has already been booked!");
                    return;
                }
        }
         if (pos==-1) {
            JOptionPane.showMessageDialog(null, "Please select a row!");
            return;
        }
         row = jRow.getSelectedIndex();
        JOptionPane.showMessageDialog(null, "You have successfully selected a seat!");
    }//GEN-LAST:event_btnSelectRowActionPerformed

    private boolean flightPatternCorrect() {
        Pattern p = Pattern.compile("^[A-Z][A-Z][0-9][0-9][0-9]$");
        Matcher m = p.matcher(txtFlightNumber.getText());
        boolean b = m.matches();
        return b;
    }
    
    private void setSeatPosition() {
        jPos.removeAllItems();
        jPos.addItem("Left window");
        jPos.addItem("Left middle");
        jPos.addItem("Left aisle");
        jPos.addItem("Right aisle");
        jPos.addItem("Right middle");
        jPos.addItem("Right window");
    }
    
    private void setSeatRow() {
        jRow.removeAllItems();
        int judgevar = 0;
        for (int i=0; i<25; i++) { 
            for (int j=0; j<customerDirectory.getCustomerList().size(); j++) {
                int[] seatNum=customerDirectory.getCustomerList().get(j).getSeat();
                if(txtFlightNumber.getText().equals(customerDirectory.getCustomerList().get(j).getFlightNumber()) && seatNum[0]==jPos.getSelectedIndex()&& seatNum[1]==i) {
                    judgevar = 1;
                }
            }
            if (judgevar==0) {
                jRow.addItem(String.valueOf(i+1));
            }
            else jRow.addItem(String.valueOf(i+1)+ "  Booked");
            judgevar=0;
        } 
    }

