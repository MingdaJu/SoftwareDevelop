    private void btnBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBookActionPerformed
        // TODO add your handling code here:
        if (txtFlightNumber == null|| txtFlightNumber.equals("")) {
            JOptionPane.showMessageDialog(null, "Flight number can't be empty!");
            return;
        }
        if (!flightPatternCorrect()) {
            JOptionPane.showMessageDialog(null, "Flight number should be in the form like HU481");
            return;
        }
        if (txtName == null|| txtName.equals("")) {
            JOptionPane.showMessageDialog(null, "Passanger name can't be empty!");
            return;
        }
        for (int k=0; k<customerDirectory.getCustomerList().size(); k++) {
            if (txtFlightNumber.getText().equals(customerDirectory.getCustomerList().get(k).getFlightNumber()) && txtName.getText().equals(customerDirectory.getCustomerList().get(k).getName())) {
            JOptionPane.showMessageDialog(null, "This customer has already booked a  ticket for this flight!");
            return;
            }
        }
        if (pos==-1||row==-1) {
            JOptionPane.showMessageDialog(null, "Please select a seat!");
        }
        Customer customer = new Customer();
        customer.setFlightNumber(txtFlightNumber.getText());
        customer.setName(txtName.getText());
        int[] temp = new int[] {pos,row};
        customer.setSeat(temp);
        customerDirectory.getCustomerList().add(customer);
        
        JOptionPane.showMessageDialog(null, "You have successfully booked a ticket!");
        txtFlightNumber.setText("");
        txtName.setText("");
        jRow.setEnabled(false);
        jRow.removeAllItems();
    }//GEN-LAST:event_btnBookActionPerformed

    private void btnSelectPosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelectPosActionPerformed
        // TODO add your handling code here:
        pos = jPos.getSelectedIndex();
        if (txtFlightNumber == null|| txtFlightNumber.equals("")) {
            JOptionPane.showMessageDialog(null, "Flight number can't be empty!");
            return;
        }
        if (!flightPatternCorrect()) {
            JOptionPane.showMessageDialog(null, "Flight number should be in the form like HU481");
            return;
        }
        if (pos==-1) {
            JOptionPane.showMessageDialog(null, "Please select a position first!");
            return;
        }
        setSeatRow();
        jRow.setEnabled(true);  
    }//GEN-LAST:event_btnSelectPosActionPerformed

