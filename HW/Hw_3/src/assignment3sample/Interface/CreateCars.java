    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        
        String brand=txtBrand.getText();
        if(brand==null||brand.equals("")){
        JOptionPane.showMessageDialog(null, "Please input a brand");
        return;
        }
        
        try{
        Integer.parseInt(txtYear.getText());
        }catch(NumberFormatException e){
        JOptionPane.showMessageDialog(null, "Please input a Year Number");
        return;    
        }
        int manufactured_year=Integer.parseInt(txtYear.getText());
        try{
        Integer.parseInt(txtMinSeats.getText());
        }catch(NumberFormatException e){
        JOptionPane.showMessageDialog(null, "Please input a MinSeat Number");
        return;    
        }
        int min_seats=Integer.parseInt(txtMinSeats.getText());
        int max_seats=Integer.parseInt(txtMaxSeats.getText());
        try{
        Integer.parseInt(txtSerialNumber.getText());
        }catch(NumberFormatException e){
        JOptionPane.showMessageDialog(null, "Please input a SerialNumber");
        return;    
        }
        int serial_num=Integer.parseInt(txtSerialNumber.getText());
        try{
        Integer.parseInt(txtMaxSeats.getText());
        }catch(NumberFormatException e){
        JOptionPane.showMessageDialog(null, "Please input a MaxSeat Number");
        return;    
        }
        
        String model_num=txtModelNumber.getText();
        if(model_num==null||model_num.equals("")){
        JOptionPane.showMessageDialog(null, "Please input a ModelNumber");
        return;
        }
        
        String available_city=txtCity.getText();
        if(available_city==null||available_city.equals("")){
        JOptionPane.showMessageDialog(null, "Please input a City");
        return;
        }
       Car car = new Car();
       Date now=new Date();
       carInformationList.setDate(now);
       car.setAvailable(checkAvailable.isSelected());
       car.setMaintenance_certificate(checkCertificate.isSelected());
       car.setBrand(brand);
       car.setAvailble_city(available_city);
       car.setMin_seats(min_seats);
       car.setMax_seats(max_seats);
       car.setSerial_num(serial_num);
       car.setModel_num(model_num);
       car.setManufactured_year(manufactured_year);
        JOptionPane.showMessageDialog(null,"Account auccessfully created");
        car.setCreatedOn(now);
        carInformationList.getCarList().add(car);
        
     
    }//GEN-LAST:event_btnCreateActionPerformed

